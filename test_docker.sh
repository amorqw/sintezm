#!/bin/bash
# Скрипт для тестирования Docker развертывания

echo "🐳 Тестирование Docker развертывания..."

# Остановить и удалить старые контейнеры
echo "🧹 Очистка старых контейнеров..."
docker-compose down -v

# Собрать и запустить
echo "🏗️  Сборка и запуск контейнеров..."
docker-compose up -d

# Подождать запуска
echo "⏳ Ожидание запуска..."
sleep 15

# Проверить статус
echo "📊 Статус контейнеров:"
docker-compose ps

# Проверить логи базы данных
echo "🗄️  Логи PostgreSQL:"
docker-compose logs db | tail -10

# Проверить логи приложения
echo "🚀 Логи Django приложения:"
docker-compose logs app | tail -10

# Проверить доступность
echo "🌐 Проверка доступности приложения..."
curl -f http://localhost:8000/health/ && echo "✅ Приложение работает!" || echo "❌ Приложение не отвечает"

echo "🎉 Тестирование завершено!"